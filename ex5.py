# Exercicio 5 - Calcule a área de um terreno retangular e informe as medidas de largura e comprimento.

# Usuário informa a largura e comprimento do terreno.
larg = float(input('Informe a largura do terreno: '))
comp = float(input('Informe o comprimento do terreno: '))

# O cálculo é feito de forma direta na saída de dados e já é dado os dados em centímetros quadrados.
print('A área deste terreno é:', larg*comp, 'cm²')